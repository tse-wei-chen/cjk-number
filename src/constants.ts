export const SEQUENCE_SYMBOL_TO_NUMBER: Record<string, number> = (() => {
  const map: Record<string, number> = {};
  const put = (chars: readonly string[]) => {
    chars.forEach((char, index) => {
      map[char] = index + 1;
    });
  };

  put(STEMS);
  put(BRANCHES);
  put(HIRAGANA);
  put(HIRAGANA_IROHA);
  put(KATAKANA);
  put(KATAKANA_IROHA);

  return map;
})();

